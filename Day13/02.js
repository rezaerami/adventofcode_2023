const grids = input.trim().split("\n\n");

const getGrid = (grid) => grid.trim().split("\n").map(line => line.trim().split(""))

const getRow = (grid, index) => grid?.[index];
const getCol = (grid, index) => grid?.map(row => row?.[index]);

const isReflection = (from, to, tolerance) => from.map((char, index) => char === to[index]).filter(item => item === false).length <= tolerance

const isMirror = (grid, index, get, limit, tolerance = 0) => {
    let previousIndex = index
    let nextIndex = index + 1

    const result = []
    while (previousIndex >= 0 && nextIndex < limit) {
        const previous = get(grid, previousIndex);
        const next = get(grid, nextIndex);
        result.push(isReflection(previous, next, tolerance));

        previousIndex--;
        nextIndex++;
    }

    return !result.includes(false);
}

const getReflectionPoints = (grid) => {
    const rowsCount = grid.length;
    const colsCount = grid[0].length

    let row = 0;
    let col = 0;
    const smudgedRows = [];
    const smudgedCols = [];

    for (let i = 0; i < rowsCount-1; i++) {
        if (isMirror(grid, i, getRow, rowsCount)) {
            row = i + 1;
            break;
        }
    }
    for (let i = 0; i < colsCount-1; i++) {
        if (isMirror(grid, i, getCol, colsCount)) {
            col = i+1;
            break;
        }
    }

    for (let i = 0; i < rowsCount - 1; i++) {
        if (isMirror(grid, i, getRow, rowsCount, 1)) {
            smudgedRows.push(i + 1);
            i = i+1;
        }
    }
    for (let i = 0; i < colsCount-1; i++) {
        if (isMirror(grid, i, getCol, colsCount, 1)) {
            smudgedCols.push(i + 1);
            i = i+1;
        }
    }

    const smudgedRow = smudgedRows.find(item => item !== row) ?? row;
    const smudgedCol = smudgedCols.find(item => item !== col) ?? col;

    if(row === smudgedRow)
        return {col: smudgedCol, row: 0}
    if(col === smudgedCol)
        return {col: 0, row: smudgedRow}

    return { col, row }

}

const reflections = grids.map(grid => getReflectionPoints(getGrid(grid)))

const result = reflections
    .reduce((result, { col, row }) => (
        result + (col+(row*100))
    ), 0)

