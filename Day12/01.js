const getCombinations = (pattern, numbers) => {
    let result = 0;
    const stack = [{ current: '', index: 0 }];

    while (stack.length > 0) {
        const { current, index } = stack.pop();

        if (index !== pattern.length) {
            if (pattern[index] === '?') {
                stack.push({ current: current + '.', index: index + 1 });
                stack.push({ current: current + '#', index: index + 1 });
            } else {
                stack.push({ current: current + pattern[index], index: index + 1 });
            }
        } else {
            const counts = current.match(/#+/g)?.map(item => item.length);
            if (counts?.join(',') === numbers?.join(',')) {
                result++;
            }
        }
    }

    return result;
};

